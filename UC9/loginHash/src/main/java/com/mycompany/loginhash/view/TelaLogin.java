    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
       String email = emailField.getText();
     char[] charSenha = passwordField.getPassword();
     
     Usuario user = UsuarioController.verificarUsuario(email, charSenha);
     
   if (user != null) {
       JOptionPane.showMessageDialog(this, "seja bem-vindo(a)! ");
       new TelaUsuario(user).setVisible(true);
       this.dispose();
       
       }   
    }//GEN-LAST:event_btnEnterActionPerformed

