    private void btnJokeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJokeActionPerformed
          try {
        var joke = controller.makeAJoke("Any");

        // Caso a piada tenha duas partes (setup + delivery)
        if (joke.getDelivery() != null && !joke.getDelivery().isEmpty()) {
            txtPiada2.setText(joke.getSetup());
            txtPiada.setText(joke.getDelivery());
        } else {
            // Piada de uma parte s√≥
            txtPiada2.setText("");
            txtPiada.setText(joke.getSetup());
        }

    } catch (Exception e) {
        txtPiada2.setText("Erro ao buscar piada");
        txtPiada.setText(e.getMessage());
    }
    }//GEN-LAST:event_btnJokeActionPerformed

